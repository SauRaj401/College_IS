   private double BinarySearch(double[] a, double x) { 
 //data array is passed to a array.
 Arrays.sort(a);//sorting the value in ascending order
 System.out.println(Arrays.toString(a));
 int Size = a.length;
 int start = 0;
 int end = Size - 1;
 while (start <= end) {
 int mid = (start + end) / 2;
 if (a[mid] == x) {
 return a[mid];
 } else if (x > a[mid]) {
 start = mid + 1;
 } else {
 end = mid - 1;
 }
 }
 return -1;
 }
